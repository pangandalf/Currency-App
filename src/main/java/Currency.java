public class Currency {
	String currency;
	String code;
	Float mid;
	
	public String getCurrency() {
		return currency;
	}
	public void setCurrency(String currency) {
		this.currency = currency;
	}
	
	public String getCode() {
		return code;
	}
	public void setCode(String code) {
		this.code = code;
	}
	
	public Float getMid() {
		return mid;
	}
	public void setMid(Float mid) {
		this.mid = mid;
	}
}